const producto = "Monitor 20 pulgadas";

// Metodos para los Strings

// Length - Obtiene el numero de caracteres de una cadena y devuelve un numero
console.log(producto.length);

// indexOf - Busca un caracter en una cadena y regresa su indice
console.log(producto.indexOf("pulgadas"));

// includes - Busca un caracter en una cadena y regresa un booleano
console.log(producto.includes("pulgadas"));
